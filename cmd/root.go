// initPath set current dir to viper
func initPath() {
	currentDir, err := filepath.Abs(os.Args[0])
	if err != nil {
		log.Panicf("cannot get current directory info")
	}
	common.CurrentDir = path.Dir(currentDir)

	homeDir, err := homedir.Dir()
	if err != nil {
		log.Panicf("detect home dir failed")
	}
	common.HomeDir = homeDir
}

// initConfig reads in config file and ENV variables if set.
func initConfig() {
	if cfgFile != "" {
		// Use config file from the flag.
		viper.SetConfigFile(cfgFile)
	} else {
		// Find home directory.
		home, err := homedir.Dir()
		if err != nil {
			fmt.Println(err)
			os.Exit(1)
		}

		// Search config in home directory with name ".invtools" (without extension).
		viper.AddConfigPath(home)
		viper.SetConfigName(".invtools")
	}

	viper.AutomaticEnv() // read in environment variables that match

	// If a config file is found, read it in.
	if err := viper.ReadInConfig(); err == nil {
		fmt.Println("Using config file:", viper.ConfigFileUsed())
	}
}
